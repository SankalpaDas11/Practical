class Square {
public static void main(String ...args) {
System.out.println("The length of the sides are : ");
int side = 10; // initialize 1st local variable
int perimeter = (side * 4); // logic to find perimeter
int area = (side * side); // logic to find area
System.out.println(side); // printing the length of sides
System.out.println("The perimeter is : " + perimeter); // printing the result of perimeter
System.out.println("The area is : " + area); // printing the result of area
}
}